#include "3-calc.h"

/**
 * op_add - add a
 * @a: a
 * @b: b
 * Return: int
*/
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - add a
 * @a: a
 * @b: b
 * Return: int
*/
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - add a
 * @a: a
 * @b: b
 * Return: int
*/
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - add a
 * @a: a
 * @b: b
 * Return: int
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - add a
 * @a: a
 * @b: b
 * Return: int
*/
int op_mod(int a, int b)
{
return (a % b);
}
